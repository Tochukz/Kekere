    <p>Kekere is a light weight minimal framework build for simplicity.  Built primarily for fun and learning but can be used for small projects.
        The framework follows many of <a href="http://laravel.com">Laravel's</a> convention and has a similar directory structure to <a  href="http://asp.net/mvc">ASP.NET MVC</a>. 
    Kekere is built for simple projects that requires some sort of MVC struture and elegance that an MVC application offers while still maintaining a minimal foot print, hence the framework is stand alone having no dependency or vendor folder.</p>
    <p>
       The basic features of an MVC application is implemented in a simple way. Some of the features include.        
        <ul>
          <li>Simple and elegant implementation of the MVC architecture</li>
          <li>Easy defination of models</li>
          <li>Easy interaction with database without defining a model</li>
          <li>Built-in view engine </li>
          <li>Built in database migration</li>
          <li>Built in seeder</li>
          <li>Extendable CLI component</li>
       </ul>
    </p>
